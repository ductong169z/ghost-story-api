package api

import (
	"gfly/app/http"
	"github.com/gflydev/core"
)

// ====================================================================
// ======================== Controller Creation =======================
// ====================================================================

// ListApi API struct.
type ListApi struct {
	core.Api
}

// ====================================================================
// ======================== Request Validation ========================
// ====================================================================

func (h *ListApi) Validate(c *core.Ctx) error {
	return http.ValidateFilter(c)
}

// ====================================================================
// ========================= Request Handling =========================
// ====================================================================

func (h *ListApi) Handle(c *core.Ctx) error {
	return c.NoContent()
}
